e = 65537

# 総当たりで16bit素数qを探す
for q in range(2, 2**16):
    if n % q == 0 and isPrime(q):
        p = n // q
        if isPrime(p):  # 念のため確認（pが512bitの素数か）
            break

phi = (p - 1) * (q - 1)
d = inverse(e, phi)
m = pow(c, d, n)

flag = long_to_bytes(m)
